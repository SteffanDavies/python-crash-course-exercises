# This script will demonstrate string functions for manipulating character case.

name = "Steffan davies"

print(f"Username:\n\t{name}")
print(f"Lowercase:\n\t{name.lower()}")
print(f"Uppercase:\n\t{name.upper()}")
print(f"Title:\n\t{name.title()}")